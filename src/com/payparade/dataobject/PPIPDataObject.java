package com.payparade.dataobject;

public class PPIPDataObject {

	private String ip_address;
	private String latitude;
	private String longitude;
	private String postal;
	private String city;
	private String region;
	private String country;
	private String metro_code;
	private String area_code;
	private String isp;
	private String org;

	public String getIp_address() {
		return ip_address;
	}

	public void setIp_address(String ipAddress) {
		ip_address = ipAddress;
	}

	public String getLatitude() {
		return latitude;
	}

	public void setLatitude(String latitude) {
		this.latitude = latitude;
	}

	public String getLongitude() {
		return longitude;
	}

	public void setLongitude(String longitude) {
		this.longitude = longitude;
	}

	public String getPostal() {
		return postal;
	}

	public void setPostal(String postal) {
		this.postal = postal;
	}

	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}

	public String getRegion() {
		return region;
	}

	public void setRegion(String region) {
		this.region = region;
	}

	public String getCountry() {
		return country;
	}

	public void setCountry(String country) {
		this.country = country;
	}

	public String getMetro_code() {
		return metro_code;
	}

	public void setMetro_code(String metroCode) {
		metro_code = metroCode;
	}

	public String getArea_code() {
		return area_code;
	}

	public void setArea_code(String areaCode) {
		area_code = areaCode;
	}

	public String getIsp() {
		return isp;
	}

	public void setIsp(String isp) {
		this.isp = isp;
	}

	public String getOrg() {
		return org;
	}

	public void setOrg(String org) {
		this.org = org;
	}

}
